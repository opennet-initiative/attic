package de.opennetinitiative.useradmin.model;

public class Location {
	private float mLat;
	private float mLong;
	private float mAlti;
	/**
	 * @return Returns the mAlti.
	 */
	public float getAlti() {
		return mAlti;
	}
	/**
	 * @param alti The mAlti to set.
	 */
	public void setAlti(float alti) {
		mAlti = alti;
	}
	/**
	 * @return Returns the mLat.
	 */
	public float getLat() {
		return mLat;
	}
	/**
	 * @param lat The mLat to set.
	 */
	public void setLat(float lat) {
		mLat = lat;
	}
	/**
	 * @return Returns the mLong.
	 */
	public float getLong() {
		return mLong;
	}
	/**
	 * @param long1 The mLong to set.
	 */
	public void setLong(float long1) {
		mLong = long1;
	}
}
